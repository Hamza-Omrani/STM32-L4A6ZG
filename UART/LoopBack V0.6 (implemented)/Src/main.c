/* USER CODE BEGIN 4 */
void vTask1(void *pvParameters)
{
  // const TickType_t xDelayInMs = pdMS_TO_TICKS(100);
  uint8_t index = 0;

  for(;;)
  {
    if(xQueueReceive(xQueue, &Rx_Data, 0))
    {
      /* Perform any processing necessitated by the interrupt. */
      Rx_Queue_Message[index] = Rx_Data;
      if (Rx_Data == LF)
      {
        // Echo back recieved data
        HAL_UART_Transmit(&hlpuart1, Rx_Queue_Message, index, 100);
        if (memcmp(Rx_Queue_Message, Start_Data, Start_Data_Size) == 0)
        {
          // Start timer
          if (HAL_TIM_Base_Start_IT(&htim7) == HAL_OK)
          {
            HAL_UART_Transmit_IT(&hlpuart1, "\r\nStarting Timer\r\n", 18);
          }
        }
        else if (memcmp(Rx_Queue_Message, Stop_Data, Stop_Data_Size) == 0)
        {
          // Stop timer
          if (HAL_TIM_Base_Stop_IT(&htim7) == HAL_OK)
          {
            HAL_UART_Transmit_IT(&hlpuart1, "\r\nStopping Timer\r\n", 18);
            uTimerCounter = 0;
          }
        }
        memset(Rx_Queue_Message, 0, RxData_Size);
        index = 0;
      }
      else
      {
        index++;
      }
    }
    // vTaskDelay(xDelayInMs);
  }
}

